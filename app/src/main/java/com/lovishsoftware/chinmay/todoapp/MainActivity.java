    /**
     * The {@link ViewPager} that will host the section contents.
     */
    private ViewPager mViewPager;
    private SectionsPagerAdapter mSectionsPagerAdapter;
    private  static  final  int code=10;
    private ItemList dataList;
    List<ItemList> itemList=new ArrayList();
    private RecyclerView recyclerView;
    private ItemListAdapter itemAdapter;
    DbListHelper dbListHelper;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        Toolbar toolbar = (Toolbar) findViewById(R.id.toolbar);
        setSupportActionBar(toolbar);
        // Create the adapter that will return a fragment for each of the three
        // primary sections of the activity.
        mSectionsPagerAdapter = new SectionsPagerAdapter(getSupportFragmentManager());

        // Set up the ViewPager with the sections adapter.
        mViewPager = (ViewPager) findViewById(R.id.container);
        mViewPager.setAdapter(mSectionsPagerAdapter);

        TabLayout tabLayout = (TabLayout) findViewById(R.id.tabs);
        tabLayout.setupWithViewPager(mViewPager);

        FloatingActionButton fab = (FloatingActionButton) findViewById(R.id.fab);
        fab.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {

                Intent intent=new Intent(MainActivity.this,SecondActivity.class);
                Log.e("MainActivity", "Inside MainActivity");
                startActivityForResult(intent,code);
            }
        });

        dbListHelper =new DbListHelper(this);
        recyclerView = (RecyclerView) findViewById(R.id.recyclerview);
        itemAdapter = new ItemListAdapter(itemList);
        RecyclerView.LayoutManager iLayoutManager = new LinearLayoutManager(MainActivity.this);
        recyclerView.setLayoutManager(iLayoutManager);
        Log.e("MainActivity", "setLayoutManager finished");
        recyclerView.setAdapter(itemAdapter);
        Log.e("MainActivity", "setAdapter finished");

    }



    @Override
    protected void onActivityResult(int requestCode, int resultCode, Intent data) {
        super.onActivityResult(requestCode, resultCode, data);

        if(requestCode == 10)
        {
            if(resultCode == Activity.RESULT_OK)
            {
                Log.e("MainActivity", "Inside onActivityResult");
                Cursor cursor=dbListHelper.getData();
                while (cursor.moveToNext())
                {
                    Log.e("MainActivity", "Inside While");
                    dataList=new ItemList();
                    dataList.setListId(cursor.getInt(0));
                    dataList.setDataList(cursor.getString(1));
                    dataList.setDone(cursor.getInt(2));
                    itemList.add(dataList);
                    itemAdapter.notifyItemInserted(itemList.size());

                }

            }
            if(resultCode == Activity.RESULT_CANCELED)
            {

            }
        }
    }

